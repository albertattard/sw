pub(crate) fn render_markdown(
    runbook: &Value,
    runbook_path: &Path,
    verbose: bool,
    verbose_mode: VerboseMode,
    debug: bool,
) -> Result<String, RenderError> {
    let entries = runbook
        .get("entries")
        .and_then(Value::as_array)
        .ok_or_else(|| {
            RenderError::Operational("Runbook is missing an entries array".to_string())
        })?;

    let mut sections = Vec::new();
    let mut cleanups: Vec<CleanupBlock> = Vec::new();
    let mut patch_restore_stack = Vec::new();
    let mut patch_snapshots = HashMap::new();
    let mut failure: Option<RenderError> = None;
    let mut state = RenderState {
        datetime_anchors: HashMap::new(),
        captured_values: HashMap::new(),
        debug_all_commands: debug,
    };
    let mut progress = ProgressReporter::new(entries.len(), verbose, verbose_mode);

    run_prerequisite_checks(entries)?;

    for (index, entry) in entries.iter().enumerate() {
        let entry_type = entry.get("type").and_then(Value::as_str).ok_or_else(|| {
            RenderError::Operational("Runbook entry is missing a type".to_string())
        })?;
        let progress_line = progress.start_entry(index, entry, runbook_path);

        let rendered = match entry_type {
            "Heading" => RenderSection::Ready(render_heading(entry)?),
            "Markdown" => RenderSection::DeferredMarkdown {
                entry: entry.clone(),
                lines: markdown_lines(entry)?,
            },
            "DisplayFile" => RenderSection::Ready(render_display_file(entry, runbook_path)?),
            "Prerequisite" => RenderSection::Ready(render_prerequisite_entry(entry)?),
            "Patch" => match render_patch(
                entry,
                runbook_path,
                &mut patch_restore_stack,
                &mut patch_snapshots,
            ) {
                Ok(section) => RenderSection::Ready(section),
                Err(err) => {
                    progress.finish_entry(progress_line);
                    failure = Some(err);
                    break;
                }
            },
            "Command" => {
                let cleanup = cleanup_block(entry, runbook_path)?;

                match render_command(entry, runbook_path, &mut state, cleanup.as_ref()) {
                    Ok(section) => {
                        if let Some(cleanup) = cleanup {
                            cleanups.push(cleanup);
                        }
                        RenderSection::Ready(section)
                    }
                    Err(RenderError::Timeout {
                        message,
                        partial_markdown,
                    }) => {
                        progress.finish_entry(progress_line);
                        sections.push(RenderSection::Ready(partial_markdown));
                        failure = Some(RenderError::Timeout {
                            message,
                            partial_markdown: String::new(),
                        });
                        break;
                    }
                    Err(err) => {
                        if let Some(cleanup) = cleanup {
                            cleanups.push(cleanup);
                        }
                        progress.finish_entry(progress_line);
                        failure = Some(err);
                        break;
                    }
                }
            }
            other => {
                return Err(RenderError::Operational(format!(
                    "Unsupported entry type `{other}`"
                )));
            }
        };

        progress.finish_entry(progress_line);
        sections.push(rendered);
    }

    let rendered_sections = render_sections(&sections, &state.captured_values, failure.is_none())?;

    let mut output = GENERATED_MARKER.to_string();
    if !rendered_sections.is_empty() {
        output.push_str("\n\n");
        output.push_str(&rendered_sections.join("\n\n"));
    }
    if !output.ends_with('\n') {
        output.push('\n');
    }

    let cleanup_failures = run_cleanup_blocks(&cleanups);
    let patch_restore_failures = run_patch_restores(&patch_restore_stack, &patch_snapshots);
    let cleanup_message = post_run_failure_message("Cleanup failed", &cleanup_failures);
    let patch_restore_message =
        post_run_failure_message("Patch restore failed", &patch_restore_failures);
    let post_run_message =
        combine_optional_messages(cleanup_message.as_deref(), patch_restore_message.as_deref());

    match failure {
        Some(RenderError::Timeout { message, .. }) => Err(RenderError::Timeout {
            message: combine_messages(&message, post_run_message.as_deref()),
            partial_markdown: output,
        }),
        Some(RenderError::CommandFailed(message)) => Err(RenderError::CommandFailed(
            combine_messages(&message, post_run_message.as_deref()),
        )),
        Some(RenderError::Operational(message)) => Err(RenderError::Operational(combine_messages(
            &message,
            post_run_message.as_deref(),
        ))),
        Some(RenderError::CleanupFailed { .. }) => unreachable!(),
        None => match post_run_message {
            Some(message) => Err(RenderError::CleanupFailed {
                message,
                markdown: output,
            }),
            None => Ok(output),
        },
    }
}

#[derive(Clone, Copy)]
enum ProgressMode {
    Off,
    Plain,
    Live,
}

struct ProgressReporter {
    total_entries: usize,
    mode: ProgressMode,
}

struct EntryProgress {
    line: String,
    started_at: Instant,
    timeout: Option<String>,
    stop_signal: Option<Arc<AtomicBool>>,
    thread: Option<thread::JoinHandle<()>>,
}

impl ProgressReporter {
    fn new(total_entries: usize, verbose: bool, verbose_mode: VerboseMode) -> Self {
        let mode = resolve_progress_mode(verbose, verbose_mode, io::stderr().is_terminal());

        Self {
            total_entries,
            mode,
        }
    }

    fn start_entry(
        &mut self,
        index: usize,
        entry: &Value,
        runbook_path: &Path,
    ) -> Option<EntryProgress> {
        match self.mode {
            ProgressMode::Off => None,
            ProgressMode::Plain => {
                let line = format_progress_line(
                    index + 1,
                    self.total_entries,
                    entry_summary(entry, runbook_path),
                );
                let timeout = verbose_timeout_label(entry);
                let _ = writeln!(
                    io::stderr(),
                    "{}",
                    progress_start_line_text(&line, timeout.as_deref())
                );

                Some(EntryProgress {
                    line,
                    started_at: Instant::now(),
                    timeout,
                    stop_signal: None,
                    thread: None,
                })
            }
            ProgressMode::Live => {
                let line = format_progress_line(
                    index + 1,
                    self.total_entries,
                    entry_summary(entry, runbook_path),
                );
                let stop_signal = Arc::new(AtomicBool::new(false));
                let thread_stop_signal = Arc::clone(&stop_signal);
                let thread_line = line.clone();
                let thread_timeout = verbose_timeout_label(entry);
                let started_at = Instant::now();
                let thread_started_at = started_at;
                let thread = thread::spawn(move || {
                    while !thread_stop_signal.load(Ordering::Relaxed) {
                        let _ = write_live_progress_line(
                            &thread_line,
                            &format_elapsed_time(thread_started_at.elapsed()),
                            thread_timeout.as_deref(),
                        );
                        thread::sleep(StdDuration::from_millis(100));
                    }
                });

                Some(EntryProgress {
                    line,
                    started_at,
                    timeout: verbose_timeout_label(entry),
                    stop_signal: Some(stop_signal),
                    thread: Some(thread),
                })
            }
        }
    }

    fn finish_entry(&mut self, progress: Option<EntryProgress>) {
        let Some(progress) = progress else {
            return;
        };

        match self.mode {
            ProgressMode::Off => {}
            ProgressMode::Plain => {
                let _ = writeln!(
                    io::stderr(),
                    "{}",
                    progress_line_text(
                        &progress.line,
                        &format_elapsed_time(progress.started_at.elapsed()),
                        progress.timeout.as_deref(),
                    )
                );
            }
            ProgressMode::Live => {
                if let Some(stop_signal) = &progress.stop_signal {
                    stop_signal.store(true, Ordering::Relaxed);
                }
                if let Some(thread) = progress.thread {
                    let _ = thread.join();
                }
                let _ = write_live_progress_line(
                    &progress.line,
                    &format_elapsed_time(progress.started_at.elapsed()),
                    progress.timeout.as_deref(),
                );
                let _ = writeln!(io::stderr());
            }
        }
    }
}

fn write_live_progress_line(line: &str, elapsed: &str, timeout: Option<&str>) -> io::Result<()> {
    let mut stderr = io::stderr().lock();
    write!(
        &mut stderr,
        "\r\x1b[2K{}",
        progress_line_text(line, elapsed, timeout)
    )?;

    stderr.flush()
}

fn progress_start_line_text(line: &str, timeout: Option<&str>) -> String {
    match timeout {
        Some(timeout) => format!("{line} (starting / {timeout})"),
        None => format!("{line} (starting)"),
    }
}

fn progress_line_text(line: &str, elapsed: &str, timeout: Option<&str>) -> String {
    match timeout {
        Some(timeout) => format!("{line} ({elapsed} / {timeout})"),
        None => format!("{line} ({elapsed})"),
    }
}

fn resolve_progress_mode(
    verbose: bool,
    verbose_mode: VerboseMode,
    stderr_is_tty: bool,
) -> ProgressMode {
    if !verbose {
        return ProgressMode::Off;
    }

    match verbose_mode {
        VerboseMode::Auto => {
            if stderr_is_tty {
                ProgressMode::Live
            } else {
                ProgressMode::Plain
            }
        }
        VerboseMode::Live => ProgressMode::Live,
        VerboseMode::Plain => ProgressMode::Plain,
    }
}

fn format_elapsed_time(duration: StdDuration) -> String {
    if duration.as_secs() < 60 {
        return format!("{:.1}s", duration.as_secs_f64());
    }

    let minutes = duration.as_secs() / 60;
    let seconds = duration.as_secs() % 60;
    format!("{minutes}m {seconds}s")
}

fn verbose_timeout_label(entry: &Value) -> Option<String> {
    let entry_type = entry.get("type").and_then(Value::as_str)?;
    if entry_type != "Command" {
        return None;
    }

    let timeout = timeout_label(entry);
    Some(compact_timeout_label(&timeout))
}

fn compact_timeout_label(timeout: &str) -> String {
    let parts: Vec<_> = timeout.split_whitespace().collect();
    if parts.len() != 2 {
        return timeout.to_string();
    }

    let value = parts[0];
    match parts[1].to_ascii_lowercase().as_str() {
        "second" | "seconds" | "sec" | "secs" | "s" => format!("{value}s"),
        "minute" | "minutes" | "min" | "mins" | "m" => format!("{value}m"),
        _ => timeout.to_string(),
    }
}

fn format_progress_line(current: usize, total: usize, summary: String) -> String {
    let width = total.to_string().len();
    format!("[{current:>width$}/{total}] {summary}")
}

fn entry_summary(entry: &Value, runbook_path: &Path) -> String {
    let entry_type = entry.get("type").and_then(Value::as_str).unwrap_or("Entry");
    let summary = match entry_type {
        "Heading" => entry
            .get("title")
            .and_then(Value::as_str)
            .unwrap_or("Heading")
            .to_string(),
        "Markdown" => first_non_empty_string_line(entry.get("contents"))
            .unwrap_or_else(|| "Markdown".to_string()),
        "Command" => first_non_empty_string_line(entry.get("commands"))
            .unwrap_or_else(|| "Command".to_string()),
        "DisplayFile" => display_file_summary(entry, runbook_path),
        "Prerequisite" => prerequisite_summary(entry),
        "Patch" => patch_summary(entry, runbook_path),
        other => other.to_string(),
    };

    format!("{entry_type}: {}", truncate_progress_summary(&summary, 80))
}

fn first_non_empty_array_string(value: Option<&Value>) -> Option<String> {
    let items = value?.as_array()?;
    for item in items {
        let text = item.as_str()?.trim();
        if !text.is_empty() {
            return Some(text.to_string());
        }
    }
    None
}

fn first_non_empty_string_line(value: Option<&Value>) -> Option<String> {
    let value = value?;

    match value {
        Value::String(text) => split_multiline_string(text).into_iter().find_map(|line| {
            let text = line.trim();
            (!text.is_empty()).then(|| text.to_string())
        }),
        Value::Array(_) => first_non_empty_array_string(Some(value)),
        _ => None,
    }
}

fn display_file_summary(entry: &Value, runbook_path: &Path) -> String {
    let relative_path = entry
        .get("path")
        .and_then(Value::as_str)
        .unwrap_or("<missing path>");
    let base_dir = runbook_path.parent().unwrap_or_else(|| Path::new("."));
    let display_path = base_dir.join(relative_path);
    let start_line = entry.get("start_line").and_then(Value::as_u64);
    let line_count = entry.get("line_count").and_then(Value::as_u64);

    match (start_line, line_count) {
        (Some(start), Some(count)) => {
            format!(
                "{} (lines {start}-{})",
                display_path.display(),
                start + count.saturating_sub(1)
            )
        }
        (Some(start), None) => format!("{} (from line {start})", display_path.display()),
        (None, _) => display_path.display().to_string(),
    }
}

fn prerequisite_summary(entry: &Value) -> String {
    let checks = entry
        .get("checks")
        .and_then(Value::as_array)
        .map(|checks| checks.len())
        .unwrap_or(0);
    if checks == 1 {
        "Prerequisite checks (1 check)".to_string()
    } else {
        format!("Prerequisite checks ({checks} checks)")
    }
}

fn patch_summary(entry: &Value, runbook_path: &Path) -> String {
    let relative_path = entry
        .get("path")
        .and_then(Value::as_str)
        .unwrap_or("<missing path>");
    let base_dir = runbook_path.parent().unwrap_or_else(|| Path::new("."));
    base_dir.join(relative_path).display().to_string()
}

fn truncate_progress_summary(summary: &str, max_chars: usize) -> String {
    let truncated: String = summary.chars().take(max_chars).collect();
    if summary.chars().count() > max_chars {
        format!("{truncated}...")
    } else {
        summary.to_string()
    }
}

