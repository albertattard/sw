fn render_display_file(entry: &Value, runbook_path: &Path) -> Result<String, RenderError> {
    let relative_path = entry
        .get("path")
        .and_then(Value::as_str)
        .ok_or_else(|| RenderError::Operational("DisplayFile entry is missing path".to_string()))?;
    let base_dir = runbook_path.parent().unwrap_or_else(|| Path::new("."));
    let display_path = base_dir.join(relative_path);
    let contents = fs::read_to_string(&display_path).map_err(|err| {
        RenderError::Operational(format!("Failed to read {}: {err}", display_path.display()))
    })?;
    let contents = apply_display_file_transform(entry, contents, &display_path)?;
    let start_line = entry
        .get("start_line")
        .and_then(Value::as_u64)
        .map(|value| value as usize)
        .unwrap_or(1);
    let line_count = entry
        .get("line_count")
        .and_then(Value::as_u64)
        .map(|value| value as usize);
    let contents = slice_display_file_contents(&contents, start_line, line_count, &display_path)?;
    let contents = apply_display_file_offset(entry, contents)?;

    let mut section = format!("```{}\n", display_file_content_type(&display_path));
    section.push_str(&contents);
    if !contents.ends_with('\n') && !contents.is_empty() {
        section.push('\n');
    }
    section.push_str("```");
    apply_display_file_indent(entry, section)
}

fn apply_display_file_transform(
    entry: &Value,
    contents: String,
    display_path: &Path,
) -> Result<String, RenderError> {
    let Some(transform) = entry.get("transform") else {
        return Ok(contents);
    };

    let language = transform
        .get("language")
        .and_then(Value::as_str)
        .ok_or_else(|| {
            RenderError::Operational("DisplayFile transform is missing language".to_string())
        })?;
    let operations = transform
        .get("operations")
        .and_then(Value::as_array)
        .ok_or_else(|| {
            RenderError::Operational("DisplayFile transform is missing operations".to_string())
        })?;

    let mut transformed = contents;
    match language {
        "java" => {
            for operation in operations {
                transformed =
                    apply_java_display_file_transform(operation, transformed, display_path)?;
            }
        }
        other => {
            return Err(RenderError::Operational(format!(
                "Unsupported DisplayFile transform language `{other}`"
            )));
        }
    }

    Ok(transformed)
}

fn apply_java_display_file_transform(
    operation: &Value,
    contents: String,
    display_path: &Path,
) -> Result<String, RenderError> {
    let operation_type = operation
        .get("type")
        .and_then(Value::as_str)
        .ok_or_else(|| {
            RenderError::Operational("DisplayFile transform operation is missing type".to_string())
        })?;

    match operation_type {
        "collapse_method_body" => collapse_java_method_body(operation, contents, display_path),
        other => Err(RenderError::Operational(format!(
            "Unsupported DisplayFile transform operation `{other}`"
        ))),
    }
}

fn collapse_java_method_body(
    operation: &Value,
    contents: String,
    display_path: &Path,
) -> Result<String, RenderError> {
    let method_name = operation
        .get("name")
        .and_then(Value::as_str)
        .ok_or_else(|| {
            RenderError::Operational(
                "DisplayFile collapse_method_body operation is missing name".to_string(),
            )
        })?;
    let replacement = operation
        .get("replacement")
        .and_then(Value::as_str)
        .unwrap_or("/* Closed for brevity */");

    let matches = find_java_method_matches(&contents, method_name);
    let method = match matches.as_slice() {
        [single] => single,
        [] => {
            return Err(RenderError::CommandFailed(format!(
                "DisplayFile transform failed for {}: Java method `{method_name}` was not found",
                display_path.display()
            )));
        }
        _ => {
            return Err(RenderError::CommandFailed(format!(
                "DisplayFile transform failed for {}: Java method `{method_name}` is ambiguous",
                display_path.display()
            )));
        }
    };

    let replacement_body = format!("{{ {replacement} }}");
    let mut collapsed = String::with_capacity(contents.len());
    collapsed.push_str(&contents[..method.open_brace]);
    collapsed.push_str(&replacement_body);
    collapsed.push_str(&contents[method.close_brace + 1..]);
    Ok(collapsed)
}

#[derive(Clone, Copy)]
struct JavaMethodMatch {
    open_brace: usize,
    close_brace: usize,
}

fn find_java_method_matches(contents: &str, method_name: &str) -> Vec<JavaMethodMatch> {
    let mut matches = Vec::new();
    let mut search_index = 0;
    let needle = format!("{method_name}(");

    while let Some(relative) = contents[search_index..].find(&needle) {
        let name_index = search_index + relative;
        let Some(open_paren) = find_java_method_open_paren(contents, name_index, method_name.len())
        else {
            search_index = name_index + method_name.len();
            continue;
        };
        let Some(close_paren) = find_matching_delimiter(contents, open_paren, b'(', b')') else {
            search_index = name_index + method_name.len();
            continue;
        };
        if !looks_like_java_method_declaration(contents, name_index, open_paren) {
            search_index = name_index + method_name.len();
            continue;
        }
        let Some(open_brace) = find_java_method_body_start(contents, close_paren + 1) else {
            search_index = name_index + method_name.len();
            continue;
        };
        let Some(close_brace) = find_matching_java_brace(contents, open_brace) else {
            search_index = name_index + method_name.len();
            continue;
        };

        matches.push(JavaMethodMatch {
            open_brace,
            close_brace,
        });
        search_index = close_brace + 1;
    }

    matches
}

fn find_java_method_open_paren(
    contents: &str,
    name_index: usize,
    name_len: usize,
) -> Option<usize> {
    let mut index = name_index + name_len;
    let bytes = contents.as_bytes();
    while index < bytes.len() && bytes[index].is_ascii_whitespace() {
        index += 1;
    }
    if bytes.get(index) == Some(&b'(') {
        Some(index)
    } else {
        None
    }
}

fn looks_like_java_method_declaration(
    contents: &str,
    name_index: usize,
    open_paren: usize,
) -> bool {
    let bytes = contents.as_bytes();

    if name_index > 0 {
        let previous = bytes[name_index - 1];
        if previous.is_ascii_alphanumeric()
            || previous == b'_'
            || previous == b'.'
            || previous == b'$'
        {
            return false;
        }
    }

    let line_start = contents[..name_index]
        .rfind('\n')
        .map_or(0, |index| index + 1);
    let before_name = contents[line_start..name_index].trim();
    if before_name.is_empty() {
        return false;
    }

    let disallowed_prefixes = [
        "if", "for", "while", "switch", "catch", "new", "return", "throw",
    ];
    if disallowed_prefixes
        .iter()
        .any(|keyword| before_name.ends_with(keyword))
    {
        return false;
    }

    !contents[line_start..open_paren].contains(';')
}

fn find_java_method_body_start(contents: &str, mut index: usize) -> Option<usize> {
    let bytes = contents.as_bytes();
    while index < bytes.len() {
        match bytes[index] {
            b' ' | b'\t' | b'\r' | b'\n' => index += 1,
            b'/' if bytes.get(index + 1) == Some(&b'/') => {
                index += 2;
                while index < bytes.len() && bytes[index] != b'\n' {
                    index += 1;
                }
            }
            b'/' if bytes.get(index + 1) == Some(&b'*') => {
                index += 2;
                while index + 1 < bytes.len() && !(bytes[index] == b'*' && bytes[index + 1] == b'/')
                {
                    index += 1;
                }
                index = (index + 2).min(bytes.len());
            }
            b't' if contents[index..].starts_with("throws") => {
                index += "throws".len();
                while index < bytes.len() {
                    match bytes[index] {
                        b'{' => return Some(index),
                        b';' => return None,
                        _ => index += 1,
                    }
                }
            }
            b'{' => return Some(index),
            b';' => return None,
            _ => index += 1,
        }
    }

    None
}

fn find_matching_delimiter(
    contents: &str,
    open_index: usize,
    open: u8,
    close: u8,
) -> Option<usize> {
    let bytes = contents.as_bytes();
    let mut depth = 0usize;
    let mut index = open_index;

    while index < bytes.len() {
        match bytes[index] {
            byte if byte == open => depth += 1,
            byte if byte == close => {
                depth = depth.saturating_sub(1);
                if depth == 0 {
                    return Some(index);
                }
            }
            b'"' => index = skip_quoted_literal(bytes, index, b'"'),
            b'\'' => index = skip_quoted_literal(bytes, index, b'\''),
            b'/' if bytes.get(index + 1) == Some(&b'/') => {
                index += 2;
                while index < bytes.len() && bytes[index] != b'\n' {
                    index += 1;
                }
            }
            b'/' if bytes.get(index + 1) == Some(&b'*') => {
                index += 2;
                while index + 1 < bytes.len() && !(bytes[index] == b'*' && bytes[index + 1] == b'/')
                {
                    index += 1;
                }
                index += 1;
            }
            _ => {}
        }
        index += 1;
    }

    None
}

fn find_matching_java_brace(contents: &str, open_index: usize) -> Option<usize> {
    find_matching_delimiter(contents, open_index, b'{', b'}')
}

fn skip_quoted_literal(bytes: &[u8], mut index: usize, quote: u8) -> usize {
    index += 1;
    while index < bytes.len() {
        if bytes[index] == b'\\' {
            index += 2;
            continue;
        }
        if bytes[index] == quote {
            return index;
        }
        index += 1;
    }
    bytes.len().saturating_sub(1)
}

fn apply_display_file_indent(entry: &Value, contents: String) -> Result<String, RenderError> {
    let Some(indent) = entry.get("indent") else {
        return Ok(contents);
    };

    let indent_width = match (indent.as_u64(), indent.as_i64()) {
        (Some(width), _) => usize::try_from(width)
            .map_err(|_| RenderError::Operational("DisplayFile indent is too large".to_string()))?,
        (None, Some(width)) if width >= 0 => usize::try_from(width)
            .map_err(|_| RenderError::Operational("DisplayFile indent is too large".to_string()))?,
        _ => {
            return Err(RenderError::Operational(
                "DisplayFile indent must be a non-negative integer".to_string(),
            ));
        }
    };

    if indent_width == 0 || contents.is_empty() {
        return Ok(contents);
    }

    let ends_with_newline = contents.ends_with('\n');
    let mut adjusted = contents
        .lines()
        .map(|line| {
            if line.is_empty() {
                String::new()
            } else {
                format!("{}{}", " ".repeat(indent_width), line)
            }
        })
        .collect::<Vec<_>>()
        .join("\n");

    if ends_with_newline {
        adjusted.push('\n');
    }

    Ok(adjusted)
}

fn apply_display_file_offset(entry: &Value, contents: String) -> Result<String, RenderError> {
    let Some(offset) = entry.get("offset") else {
        return Ok(contents);
    };

    let offset_width = match (offset.as_i64(), offset.as_u64()) {
        (Some(width), _) => width,
        (None, Some(width)) => i64::try_from(width)
            .map_err(|_| RenderError::Operational("DisplayFile offset is too large".to_string()))?,
        _ => {
            return Err(RenderError::Operational(
                "DisplayFile offset must be an integer".to_string(),
            ));
        }
    };

    if offset_width == 0 || contents.is_empty() {
        return Ok(contents);
    }

    let ends_with_newline = contents.ends_with('\n');
    let mut adjusted = contents
        .lines()
        .map(|line| apply_display_file_offset_to_line(line, offset_width))
        .collect::<Vec<_>>()
        .join("\n");

    if ends_with_newline {
        adjusted.push('\n');
    }

    Ok(adjusted)
}

fn apply_display_file_offset_to_line(line: &str, offset_width: i64) -> String {
    if line.is_empty() {
        return String::new();
    }

    if offset_width > 0 {
        return format!("{}{}", " ".repeat(offset_width as usize), line);
    }

    let spaces_to_remove = offset_width.unsigned_abs() as usize;
    let removable = line
        .as_bytes()
        .iter()
        .take_while(|byte| **byte == b' ')
        .take(spaces_to_remove)
        .count();

    line[removable..].to_string()
}

fn slice_display_file_contents(
    contents: &str,
    start_line: usize,
    line_count: Option<usize>,
    display_path: &Path,
) -> Result<String, RenderError> {
    let lines: Vec<&str> = contents.lines().collect();
    let start_index = start_line.saturating_sub(1);

    if start_index >= lines.len() && !lines.is_empty() {
        return Err(RenderError::Operational(format!(
            "DisplayFile start_line {} is beyond the end of {}",
            start_line,
            display_path.display()
        )));
    }

    let end_index = match line_count {
        Some(count) => start_index.saturating_add(count).min(lines.len()),
        None => lines.len(),
    };

    let mut sliced = lines[start_index..end_index].join("\n");
    if !sliced.is_empty() && contents.ends_with('\n') && end_index == lines.len() {
        sliced.push('\n');
    }

    Ok(sliced)
}

fn render_heading(entry: &Value) -> Result<String, RenderError> {
    let level = entry
        .get("level")
        .and_then(Value::as_str)
        .ok_or_else(|| RenderError::Operational("Heading entry is missing level".to_string()))?;
    let title = entry
        .get("title")
        .and_then(Value::as_str)
        .ok_or_else(|| RenderError::Operational("Heading entry is missing title".to_string()))?;

    let marker = match level {
        "H1" => "#",
        "H2" => "##",
        "H3" => "###",
        "H4" => "####",
        "H5" => "#####",
        "H6" => "######",
        other => {
            return Err(RenderError::Operational(format!(
                "Unsupported heading level `{other}`"
            )));
        }
    };

    Ok(format!("{marker} {title}"))
}

fn markdown_lines(entry: &Value) -> Result<Vec<String>, RenderError> {
    let contents = entry.get("contents").ok_or_else(|| {
        RenderError::Operational("Markdown entry is missing contents".to_string())
    })?;

    prose_lines(
        contents,
        "Markdown contents must be a string or an array of strings",
        "Markdown contents must contain only strings",
    )
}

fn render_sections(
    sections: &[RenderSection],
    captured_values: &HashMap<String, String>,
    strict_markdown: bool,
) -> Result<Vec<String>, RenderError> {
    let missing_behavior = if strict_markdown {
        MissingCaptureBehavior::Error
    } else {
        MissingCaptureBehavior::Preserve
    };

    sections
        .iter()
        .map(|section| match section {
            RenderSection::Ready(rendered) => Ok(rendered.clone()),
            RenderSection::DeferredMarkdown { entry, lines } => {
                render_markdown_lines(entry, lines, captured_values, missing_behavior)
            }
        })
        .collect()
}

fn render_markdown_lines(
    entry: &Value,
    lines: &[String],
    captured_values: &HashMap<String, String>,
    missing_behavior: MissingCaptureBehavior,
) -> Result<String, RenderError> {
    interpolate_lines(lines, captured_values, "Markdown", missing_behavior)
        .map(|lines| lines.join("\n"))
        .and_then(|section| apply_indent(entry, section, "Markdown"))
}

fn interpolate_command_lines(
    command_lines: &[String],
    captured_values: &HashMap<String, String>,
) -> Result<Vec<String>, RenderError> {
    interpolate_lines(
        command_lines,
        captured_values,
        "Command",
        MissingCaptureBehavior::Error,
    )
}

fn render_patch(
    entry: &Value,
    runbook_path: &Path,
    patch_restore_stack: &mut Vec<std::path::PathBuf>,
    patch_snapshots: &mut HashMap<std::path::PathBuf, Vec<u8>>,
) -> Result<String, RenderError> {
    let lines = entry
        .get("patch")
        .map(|patch| {
            string_lines(
                patch,
                "Patch patch must be a string or an array of strings",
                "Patch lines must contain only strings",
            )
        })
        .transpose()?
        .ok_or_else(|| RenderError::Operational("Patch entry is missing patch".to_string()))?;

    let (target_path, relative_path) = patch_target_path(entry, runbook_path)?;
    patch_snapshots
        .entry(target_path.clone())
        .or_insert(snapshot_patch_target(&target_path)?);
    patch_restore_stack.push(target_path);
    apply_patch_entry(runbook_path, &relative_path, &lines)?;

    let mut section = String::from("```diff\n");
    section.push_str(&lines.join("\n"));
    if !lines.is_empty() {
        section.push('\n');
    }
    section.push_str("```");
    apply_indent(entry, section, "Patch")
}

fn render_command(
    entry: &Value,
    runbook_path: &Path,
    state: &mut RenderState,
    timeout_cleanup: Option<&CleanupBlock>,
) -> Result<String, RenderError> {
    let commands = entry
        .get("commands")
        .ok_or_else(|| RenderError::Operational("Command entry is missing commands".to_string()))?;
    let command_lines = string_lines(
        commands,
        "Command commands must be a string or an array of strings",
        "Command list must contain only strings",
    )?;

    let resolved_command_lines = interpolate_command_lines(&command_lines, &state.captured_values)?;
    let command_text = resolved_command_lines.join("\n");
    let rendered_command_text = render_command_text(entry, &command_text)?;
    let mut section = format!("```shell\n{rendered_command_text}\n```");
    let execution = execute_command(entry, &command_text, runbook_path, timeout_cleanup)?;
    let debug_enabled = command_debug_enabled(entry, state.debug_all_commands);
    let mut debug_lines = Vec::new();
    if debug_enabled {
        debug_lines.push("[debug] Command entry:".to_string());
        debug_lines.push(format_command_entry_for_debug(entry));
        debug_lines.push("[debug] Raw stdout:".to_string());
        debug_lines.push(format_command_stream_for_debug(&execution.stdout));
        debug_lines.push("[debug] Raw stderr:".to_string());
        debug_lines.push(format_command_stream_for_debug(&execution.stderr));
    }
    let capture_rewrite_result = match rewritten_stdout_for_capture(entry, &execution, state) {
        Ok(result) => result,
        Err(err) => {
            emit_debug_lines(debug_enabled, &debug_lines);
            return Err(err);
        }
    };
    let rewritten_stdout = capture_rewrite_result.rendered.clone();
    let generated_captures = capture_rewrite_result.generated_captures;
    let output_stream = rendered_output_stream(entry)?;
    let render_rewrite_result = match rendered_output_for_entry(entry, &execution, state) {
        Ok(result) => result,
        Err(err) => {
            emit_debug_lines(debug_enabled, &debug_lines);
            return Err(err);
        }
    };
    if output_stream != OutputStream::Stdout && debug_enabled {
        debug_lines.push("[debug] Rewritten stdout for capture:".to_string());
        debug_lines.push(format_command_stream_for_debug(&rewritten_stdout));
    }
    debug_lines.extend(render_rewrite_result.debug_lines);
    let rendered_output = render_rewrite_result.rendered;
    if debug_enabled {
        if output_stream == OutputStream::Stdout {
            debug_lines.push("[debug] Rewritten stdout:".to_string());
            debug_lines.push(format_command_stream_for_debug(&rendered_output));
        } else {
            debug_lines.push("[debug] Rewritten rendered output stream:".to_string());
            debug_lines.push(format_command_stream_for_debug(&rendered_output));
        }
    }

    if execution.timed_out {
        emit_debug_lines(debug_enabled, &debug_lines);
        append_output(entry, &rendered_output, &mut section)?;
        return Err(RenderError::Timeout {
            message: combine_messages(
                &format!("Command timed out after {}", timeout_label(entry)),
                execution.timeout_cleanup_failure.as_deref(),
            ),
            partial_markdown: apply_indent(entry, section, "Command")?,
        });
    }

    ensure_assertions(entry, &execution, runbook_path)?;
    if let Err(err) = extract_captured_values(
        entry,
        &execution.stdout,
        &rewritten_stdout,
        &mut state.captured_values,
        &mut debug_lines,
    ) {
        emit_debug_lines(debug_enabled, &debug_lines);
        return Err(err);
    }
    store_generated_rewrite_captures(&generated_captures, &mut state.captured_values);
    if debug_enabled {
        for capture in &generated_captures {
            debug_lines.push(format!(
                "[debug] Generated capture {} = {}",
                capture.original_name, capture.original_value
            ));
            debug_lines.push(format!(
                "[debug] Generated capture {} = {}",
                capture.rewritten_name, capture.rewritten_value
            ));
        }
    }
    emit_debug_lines(debug_enabled, &debug_lines);
    append_output(entry, &rendered_output, &mut section)?;
    apply_indent(entry, section, "Command")
}

fn render_command_text(entry: &Value, command_text: &str) -> Result<String, RenderError> {
    let Some(working_dir) = entry.get("working_dir") else {
        return Ok(command_text.to_string());
    };

    let working_dir = working_dir.as_str().ok_or_else(|| {
        RenderError::Operational("Command working_dir must be a string".to_string())
    })?;
    let quoted_working_dir = shell_single_quote(working_dir);
    let indented_command = command_text
        .lines()
        .map(|line| format!("  {line}"))
        .collect::<Vec<_>>()
        .join("\n");

    Ok(format!(
        "(\n  cd {quoted_working_dir} &&\n{indented_command}\n)"
    ))
}

fn shell_single_quote(value: &str) -> String {
    format!("'{}'", value.replace('\'', r#"'"'"'"#))
}

fn command_debug_enabled(entry: &Value, debug_all_commands: bool) -> bool {
    if debug_all_commands {
        return true;
    }

    entry.get("debug").and_then(Value::as_bool).unwrap_or(false)
}

fn append_output(
    entry: &Value,
    rendered_output: &str,
    section: &mut String,
) -> Result<(), RenderError> {
    let Some(output) = entry.get("output") else {
        return Ok(());
    };

    if let Some(caption) = output.get("caption") {
        let caption_text = render_caption(caption)?;
        section.push_str("\n\n");
        section.push_str(&caption_text);
    }

    section.push_str("\n\n");
    section.push_str(output_fence_open(output)?);
    section.push_str(rendered_output);
    if !rendered_output.ends_with('\n') && !rendered_output.is_empty() {
        section.push('\n');
    }
    section.push_str("```");
    Ok(())
}

fn render_caption(caption: &Value) -> Result<String, RenderError> {
    match caption {
        Value::String(text) => Ok(split_multiline_string(text).join("\n")),
        Value::Array(items) => {
            let mut lines = Vec::new();
            for item in items {
                lines.push(
                    item.as_str()
                        .ok_or_else(|| {
                            RenderError::Operational(
                                "Command output caption must contain only strings".to_string(),
                            )
                        })?
                        .to_string(),
                );
            }
            Ok(lines.join("\n"))
        }
        _ => Err(RenderError::Operational(
            "Command output caption must be a string or array of strings".to_string(),
        )),
    }
}

fn render_prerequisite_entry(entry: &Value) -> Result<String, RenderError> {
    let checks = entry
        .get("checks")
        .and_then(Value::as_array)
        .ok_or_else(|| {
            RenderError::Operational("Prerequisite entry is missing checks".to_string())
        })?;

    let mut sections = Vec::new();
    for check in checks {
        let contents = check.get("contents").ok_or_else(|| {
            RenderError::Operational("Prerequisite contents are required".to_string())
        })?;
        let lines = prose_lines(
            contents,
            "Prerequisite contents must be a string or an array of strings",
            "Prerequisite contents must contain only strings",
        )?;
        sections.push(lines.join("\n"));
    }

    Ok(sections.join("\n\n"))
}

fn prose_lines(
    value: &Value,
    type_message: &str,
    item_message: &str,
) -> Result<Vec<String>, RenderError> {
    string_lines(value, type_message, item_message)
}

fn string_lines(
    value: &Value,
    type_message: &str,
    item_message: &str,
) -> Result<Vec<String>, RenderError> {
    match value {
        Value::String(text) => Ok(split_multiline_string(text)),
        Value::Array(items) => {
            let mut lines = Vec::new();
            for item in items {
                lines.push(
                    item.as_str()
                        .ok_or_else(|| RenderError::Operational(item_message.to_string()))?
                        .to_string(),
                );
            }
            Ok(lines)
        }
        _ => Err(RenderError::Operational(type_message.to_string())),
    }
}

fn split_multiline_string(value: &str) -> Vec<String> {
    value
        .split_terminator('\n')
        .map(|line| line.strip_suffix('\r').unwrap_or(line).to_string())
        .collect()
}

pub(crate) fn check_prerequisites(runbook: &Value) -> Result<(), RenderError> {
    let entries = runbook
        .get("entries")
        .and_then(Value::as_array)
        .ok_or_else(|| {
            RenderError::Operational("Runbook is missing an entries array".to_string())
        })?;
    run_prerequisite_checks(entries)
}

fn run_prerequisite_checks(entries: &[Value]) -> Result<(), RenderError> {
    for entry in entries {
        let Some("Prerequisite") = entry.get("type").and_then(Value::as_str) else {
            continue;
        };

        let checks = entry
            .get("checks")
            .and_then(Value::as_array)
            .ok_or_else(|| {
                RenderError::Operational("Prerequisite entry is missing checks".to_string())
            })?;

        for check in checks {
            run_prerequisite_check(check)?;
        }
    }

    Ok(())
}

fn run_prerequisite_check(check: &Value) -> Result<(), RenderError> {
    let name = check.get("name").and_then(Value::as_str).ok_or_else(|| {
        RenderError::Operational("Prerequisite name must be a string".to_string())
    })?;
    let kind = check.get("kind").and_then(Value::as_str).ok_or_else(|| {
        RenderError::Operational("Prerequisite kind must be a string".to_string())
    })?;

    match kind {
        "command" => run_command_prerequisite_check(check, name),
        "java" => run_java_prerequisite_check(check, name),
        other => Err(RenderError::Operational(format!(
            "Unsupported prerequisite kind `{other}`"
        ))),
    }
}

fn run_command_prerequisite_check(check: &Value, name: &str) -> Result<(), RenderError> {
    let commands = check.get("commands").ok_or_else(|| {
        RenderError::Operational("Prerequisite commands are required".to_string())
    })?;
    let lines = string_lines(
        commands,
        "Prerequisite commands must be a string or an array of strings",
        "Prerequisite commands must contain only strings",
    )?;

    let script = lines.join("\n");
    let execution = execute_command(check, &script, Path::new("."), None)?;
    if execution.timed_out {
        return Err(prerequisite_failure(
            name,
            check.get("help").and_then(Value::as_str),
            &format!("timed out after {}", timeout_label(check)),
        ));
    }

    ensure_assertions(check, &execution, Path::new(".")).map_err(|err| match err {
        RenderError::CommandFailed(message) => {
            prerequisite_failure(name, check.get("help").and_then(Value::as_str), &message)
        }
        other => other,
    })?;

    Ok(())
}

fn run_java_prerequisite_check(check: &Value, name: &str) -> Result<(), RenderError> {
    let version = check
        .get("version")
        .and_then(Value::as_str)
        .ok_or_else(|| {
            RenderError::Operational("Prerequisite Java version must be a string".to_string())
        })?;
    let requirement = parse_java_version_requirement(version).ok_or_else(|| {
        RenderError::Operational("Prerequisite Java version must be like `17` or `24+`".to_string())
    })?;
    let executable = resolve_prerequisite_java_executable(check).map_err(|detail| {
        prerequisite_failure(name, check.get("help").and_then(Value::as_str), &detail)
    })?;

    let output = Command::new(&executable)
        .arg("-version")
        .output()
        .map_err(|err| {
            prerequisite_failure(
                name,
                check.get("help").and_then(Value::as_str),
                &format!("failed to execute `{}`: {err}", executable.display()),
            )
        })?;

    let stdout = String::from_utf8_lossy(&output.stdout);
    let stderr = String::from_utf8_lossy(&output.stderr);
    let combined = format!("{stdout}\n{stderr}");
    let detected = detect_java_major_version(&combined).ok_or_else(|| {
        prerequisite_failure(
            name,
            check.get("help").and_then(Value::as_str),
            &format!(
                "could not determine Java version from `{}`",
                executable.display()
            ),
        )
    })?;

    if requirement.matches(detected) {
        return Ok(());
    }

    Err(prerequisite_failure(
        name,
        check.get("help").and_then(Value::as_str),
        &format!(
            "expected Java version `{}`, found `{detected}`",
            requirement.display()
        ),
    ))
}

fn resolve_prerequisite_java_executable(check: &Value) -> Result<std::path::PathBuf, String> {
    if let Some(java_home) = check.get("java_home").and_then(Value::as_str) {
        return prerequisite_java_home_executable(java_home);
    }

    if let Some(java_home_env) = check.get("java_home_env").and_then(Value::as_str) {
        let value = std::env::var(java_home_env)
            .map_err(|_| format!("environment variable `{java_home_env}` is not set"))?;
        return prerequisite_java_home_executable(&value);
    }

    Ok(std::path::PathBuf::from("java"))
}

fn prerequisite_java_home_executable(java_home: &str) -> Result<std::path::PathBuf, String> {
    let executable = Path::new(java_home).join("bin").join("java");
    if executable.is_file() {
        Ok(executable)
    } else {
        Err(format!(
            "Java home `{java_home}` does not contain `bin/java`"
        ))
    }
}

struct JavaVersionRequirement {
    major: u32,
    minimum: bool,
}

impl JavaVersionRequirement {
    fn matches(&self, actual: u32) -> bool {
        if self.minimum {
            actual >= self.major
        } else {
            actual == self.major
        }
    }

    fn display(&self) -> String {
        if self.minimum {
            format!("{}+", self.major)
        } else {
            self.major.to_string()
        }
    }
}

fn parse_java_version_requirement(value: &str) -> Option<JavaVersionRequirement> {
    if let Some(prefix) = value.strip_suffix('+') {
        let major = prefix.parse().ok()?;
        return Some(JavaVersionRequirement {
            major,
            minimum: true,
        });
    }

    let major = value.parse().ok()?;
    Some(JavaVersionRequirement {
        major,
        minimum: false,
    })
}

fn detect_java_major_version(output: &str) -> Option<u32> {
    let quoted =
        Regex::new(r#"version "([^"]+)""#).expect("valid java quoted version detection regex");
    if let Some(captures) = quoted.captures(output) {
        return parse_java_major_from_version_string(captures.get(1)?.as_str());
    }

    let bare = Regex::new(r#"(?m)^(?:openjdk|java)\s+([0-9][0-9A-Za-z._+-]*)"#)
        .expect("valid java bare version detection regex");
    if let Some(captures) = bare.captures(output) {
        return parse_java_major_from_version_string(captures.get(1)?.as_str());
    }

    None
}

fn parse_java_major_from_version_string(value: &str) -> Option<u32> {
    let normalized = value.strip_prefix("1.").unwrap_or(value);
    let digits: String = normalized
        .chars()
        .take_while(|character| character.is_ascii_digit())
        .collect();
    if digits.is_empty() {
        None
    } else {
        digits.parse().ok()
    }
}

fn prerequisite_failure(name: &str, help: Option<&str>, detail: &str) -> RenderError {
    let mut message = format!("Prerequisite failed: {name}: {detail}");
    if let Some(help) = help {
        message.push('\n');
        message.push_str(help);
    }
    RenderError::CommandFailed(message)
}

fn output_fence_open(output: &Value) -> Result<&'static str, RenderError> {
    match output.get("content_type").and_then(Value::as_str) {
        Some("text") | None => Ok("```\n"),
        Some("json") => Ok("```json\n"),
        Some("xml") => Ok("```xml\n"),
        Some("html") => Ok("```html\n"),
        Some("java") => Ok("```java\n"),
        Some(other) => Err(RenderError::Operational(format!(
            "Unsupported output content type `{other}`"
        ))),
    }
}

fn format_command_entry_for_debug(entry: &Value) -> String {
    serde_json::to_string_pretty(entry)
        .unwrap_or_else(|_| "<failed to serialize command entry>".to_string())
}

